namespace BlackfyreStudio\CRUD\Scope;

/**
 * Class Scope
 * @package BlackfyreStudio\CRUD\Scope
 */
class Scope
{
    /**
     * @var
     */
    protected $label;

    /**
     * @var
     */
    protected $scope;

    /**
     * @return mixed
     */
    public function getLabel()
    {
        return $this->label;
    }

    /**
     * @param mixed $label
     */
    public function setLabel($label)
    {
        $this->label = $label;
    }

    /**
     * @return mixed
     */
    public function getScope()
    {
        return $this->scope;
    }

    /**
     * @param mixed $scope
     */
    public function __construct($scope)
    {
        $this->scope = $scope;
    }
}